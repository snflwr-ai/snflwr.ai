// Required user-facing disclosures (see docs/compliance/REQUIRED_DISCLOSURES.md).
// Only the disclosures that are ACCURATE NOW are surfaced here — the AI-content
// notice, the crisis/988 notice, and the supervision/intended-use notice. The
// "agree to the Terms of Service / Privacy Policy" notices are intentionally NOT
// shown yet: those documents are still DRAFT / not-in-effect, and the operator
// (entity name/address/phone) disclosures are blocked until a business entity is
// registered. Surfacing them now would imply legal docs are in effect when they
// are not. Build text-only via el() (raw html is forbidden by the helper).

import { el } from '../core/dom.js';

const AI_NOTICE =
  'snflwr.ai is an AI tutor. Its answers are generated by an AI model and may be ' +
  'incomplete or incorrect. It is a study aid, not a replacement for a teacher, ' +
  'textbook, or a parent/guardian. Please double-check important facts.';

const CRISIS_NOTICE =
  'snflwr.ai does not provide medical, mental-health, legal, or emergency advice. ' +
  'If you or someone you know is struggling or in danger, talk to a trusted adult, ' +
  'teacher, or counselor, or call or text the 988 Suicide & Crisis Lifeline. In an ' +
  'emergency, call 911.';

const SUPERVISION_NOTICE =
  'snflwr.ai is intended for K-12 learners used with parental or school ' +
  'supervision. A parent, guardian, or school must set up the account and provide ' +
  'the consent described during setup. Formal Terms of Service and Privacy Policy ' +
  'are in preparation; per-child parental consent is collected in the consent flow.';

// Persistent, app-wide footer: the two highest-priority notices (AI-generated
// content + crisis resources). Appears under the main content on every view.
export function renderDisclosureFooter() {
  return el('footer', { class: 'disclosure-footer', role: 'contentinfo' }, [
    el('p', { class: 'disclosure-ai', text: AI_NOTICE }),
    el('p', { class: 'disclosure-crisis', text: CRISIS_NOTICE }),
  ]);
}

// Body for the Settings "Safety & Disclosures" card (the card() helper supplies
// the title). Returns a div of the three accurate-now notices.
export function renderDisclosuresBody() {
  return el('div', { class: 'settings-section disclosures-body' }, [
    el('h3', { text: 'AI-generated content' }),
    el('p', { text: AI_NOTICE }),
    el('h3', { text: 'Not professional advice — crisis resources' }),
    el('p', { text: CRISIS_NOTICE }),
    el('h3', { text: 'Intended use & supervision' }),
    el('p', { text: SUPERVISION_NOTICE }),
  ]);
}
